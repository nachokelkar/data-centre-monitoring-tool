                        <div className={classes.container}>
                            <Fade in={checked}>
                                <Paper elevation={4} className={classes.paper}>
                                    <svg className={classes.svg}>
                                        <polygon
                                            points="0,100 50,00, 100,100"
                                            className={classes.polygon}
                                        />
                                    </svg>
                                </Paper>
                            </Fade>
                        </div>
